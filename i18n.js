function normalizeText(value) {
  return value.replace(/\s+/g, " ").trim();
}

function getLang() {
  return localStorage.getItem(LANG_KEY) || "ru";
}

function setLang(lang) {
  localStorage.setItem(LANG_KEY, lang);
  applyLanguage();
  document.dispatchEvent(new CustomEvent("saqta:languagechange", { detail: { lang } }));
}

function translateText(text, lang) {
  if (lang === "ru") return text;
  const normalized = normalizeText(text);
  const stepMatch = normalized.match(/^Шаг\s+(\d+)\s+из\s+(\d+)$/);
  if (stepMatch) return `${stepMatch[1]}-қадам / ${stepMatch[2]}`;
  return kzMap[normalized] || text;
}

function translateTextNodes(root, lang) {
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      const parent = node.parentElement;
      if (!parent || ["SCRIPT", "STYLE"].includes(parent.tagName)) return NodeFilter.FILTER_REJECT;
      const text = normalizeText(node.nodeValue);
      return text ? NodeFilter.FILTER_ACCEPT : NodeFilter.FILTER_REJECT;
    },
  });

  const nodes = [];
  while (walker.nextNode()) nodes.push(walker.currentNode);

  nodes.forEach((node) => {
    if (!originalTextNodes.has(node)) {
      originalTextNodes.set(node, node.nodeValue);
    }
    const source = originalTextNodes.get(node);
    const translated = translateText(source, lang);
    if (translated !== source) {
      if (node.nodeValue !== translated) node.nodeValue = translated;
    } else if (lang === "ru") {
      if (node.nodeValue !== source) node.nodeValue = source;
    }
  });
}

function updateLangButtons(lang) {
  document.querySelectorAll("[data-lang]").forEach((button) => {
    const active = button.dataset.lang === lang;
    button.classList.toggle("is-active", active);
    button.setAttribute("aria-pressed", String(active));
  });
  document.documentElement.lang = lang === "kz" ? "kk" : "ru";
}

function applyLanguage() {
  const lang = getLang();
  updateLangButtons(lang);
  translateTextNodes(document.body, lang);
}

document.querySelectorAll("[data-lang]").forEach((button) => {
  button.addEventListener("click", () => setLang(button.dataset.lang));
});

let i18nTimer = null;
const i18nObserver = new MutationObserver(() => {
  clearTimeout(i18nTimer);
  i18nTimer = setTimeout(applyLanguage, 30);
});

document.addEventListener("DOMContentLoaded", () => {
  applyLanguage();
  i18nObserver.observe(document.body, { childList: true, subtree: true });
});

let activeArticleId = null;

function getNewsLang() {
  return localStorage.getItem("saqta-lang") || "ru";
}

function escapeHtml(value) {
  return value.replace(/[&<>"']/g, (char) => {
    const entities = { "&": "&amp;", "<": "&lt;", ">": "&gt;", '"': "&quot;", "'": "&#039;" };
    return entities[char];
  });
}

function renderBlock(block) {
  if (block.type === "h3") return `<h3>${escapeHtml(block.text)}</h3>`;
  if (block.type === "ul") {
    return `<ul>${block.items.map((item) => `<li>${escapeHtml(item)}</li>`).join("")}</ul>`;
  }
  return `<p>${escapeHtml(block.text)}</p>`;
}

function renderArticle(articleId) {
  const article = newsArticles[articleId];
  if (!article) return;
  const lang = getNewsLang();
  const content = article[lang] || article.ru;
  activeArticleId = articleId;
  articleDate.textContent = article.date;
  articleTitle.textContent = content.title;
  articleBody.innerHTML = content.body.map(renderBlock).join("");
}

function openArticle(articleId) {
  renderArticle(articleId);
  articleModal.classList.add("is-open");
  articleModal.setAttribute("aria-hidden", "false");
  document.body.classList.add("article-lock");
  articleClose.focus();
}

function closeArticle() {
  articleModal.classList.remove("is-open");
  articleModal.setAttribute("aria-hidden", "true");
  document.body.classList.remove("article-lock");
}

document.querySelectorAll("[data-news-open]").forEach((trigger) => {
  trigger.addEventListener("click", (event) => {
    event.preventDefault();
    openArticle(trigger.dataset.newsOpen);
  });
});

articleClose.addEventListener("click", closeArticle);
articleModal.addEventListener("click", (event) => {
  if (event.target === articleModal) closeArticle();
});

document.addEventListener("keydown", (event) => {
  if (event.key === "Escape" && articleModal.classList.contains("is-open")) closeArticle();
});

document.addEventListener("saqta:languagechange", () => {
  if (activeArticleId && articleModal.classList.contains("is-open")) {
    renderArticle(activeArticleId);
  }
});
